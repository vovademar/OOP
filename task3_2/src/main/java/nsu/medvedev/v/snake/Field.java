package nsu.medvedev.v.snake;

public class Field {
    int size;
    int collumns;
    int rows;

    public int getCollumns() {
        return collumns;
    }

    public int getRows() {
        return rows;
    }

    public int getSize() {
        return size;
    }

    public void setCollumns(int collumns) {
        this.collumns = collumns;
    }

    public void setRows(int rows) {
        this.rows = rows;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
